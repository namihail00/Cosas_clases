	public static int suma(int n, int m) {
		int sum = n+m;
		
		return sum;
	}
	
	public static void main(String[] args) {
		
		int sumatorio = suma(17,3);
		
		suma(17,3);
		
		System.out.println(sumatorio);
		
	
	}
